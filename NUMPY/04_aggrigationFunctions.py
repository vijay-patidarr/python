import numpy as np

arr = np.array([10,20,30,40,50])

print(np.sum(arr)) # givee sum of all element of a array
print(np.mean(arr)) # give mean of array array
print(np.max(arr)) # give the max value in the array
print(np.min(arr)) # give the minimum value present in the array
print(np.std(arr)) # give standard deviation
print(np.var(arr))
