# Arithmetic Operators Example

# define two numbers
a = 4
b = 2

# addition
print("a + b =", a + b)  

# subtraction
print("a - b =", a - b)  

# multiplication
print("a * b =", a * b)  

# division
print("a / b =", a / b)  

# modulus (remainder)
print("a % b =", a % b)  

# exponentiation (power)
print("a ** b =", a ** b)