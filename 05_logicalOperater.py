# logical operators

a = 50
b = 30

# 'and' operator: returns True if both conditions are True
print((a > 40) and (b < 40))

# 'or' operator: returns True if at least one condition is True
print((a < 40) or (b < 40))

# 'not' operator: reverses the result (True becomes False, False becomes True)
print(not (a == b))